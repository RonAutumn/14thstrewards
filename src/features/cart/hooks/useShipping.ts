import { useState } from 'react';
import { toast } from '@/components/ui/use-toast';

export function useShipping() {
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const getShippingRates = async (zipCode: string, weight: number = 1) => {
    try {
      setIsLoading(true);
      const response = await fetch('/api/shipping/rates', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          zipCode,
          weight,
        }),
      });

      if (!response.ok) {
        throw new Error('Failed to fetch shipping rates');
      }

      const data = await response.json();
      return data.rates;
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Failed to fetch shipping rates');
      toast({
        title: "Error",
        description: "Failed to fetch shipping rates. Please try again.",
        variant: "destructive",
      });
      return null;
    } finally {
      setIsLoading(false);
    }
  };

  return {
    isLoading,
    error,
    getShippingRates
  };
}

export function generateOrderId(): string {
  const timestamp = Date.now();
  const randomNum = Math.floor(Math.random() * 1000000);
  return `${timestamp % 1000000}${randomNum % 1000}`;
}

